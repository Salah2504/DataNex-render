def clean_sql(sql):
    import re

    # إزالة FETCH ROWS ONLY
    sql = re.sub(r"FETCH\s+.*?ROWS\s+ONLY", "", sql, flags=re.IGNORECASE)

    # إزالة سطر قبل ;
    sql = re.sub(r"\n\s*;\s*", ";", sql)

    return sql.strip()
#-----------------------------------------------
def fix_informix_sql(sql):
    if "FIRST" in sql.upper() and "SELECT" in sql.upper():
        # لو FIRST في الآخر → ننقلها بعد SELECT
        import re

        match = re.search(r"FIRST\s+(\d+)", sql, re.IGNORECASE)
        if match:
            n = match.group(1)
            sql = re.sub(r"FIRST\s+\d+", "", sql, flags=re.IGNORECASE)
            sql = sql.replace("SELECT", f"SELECT FIRST {n}", 1)

    return sql        
# ------------------- ASK -------------------
@app.post("/ask")
@limiter.limit("5/minute")
def ask(q: Question, request: Request):

    prompt = q.question.strip()

    if not prompt:
        raise HTTPException(status_code=400, detail="Empty prompt")

    if detect_injection(prompt):
        raise HTTPException(status_code=400, detail="⚠️ Suspicious request")

    
    if "select" in prompt.lower():
        ai_prompt = prompt
    else:
        ai_prompt = f"""
            Generate an IBM Informix SQL query.
            
            Rules:
            - Use FIRST instead of LIMIT
            - Use only this schema:
            {schema}
            - Do NOT invent columns
            - Return ONLY SQL inside ```sql``` block
            
            User request:
            {prompt}
            """
    
    
    response = ask_ai(ai_prompt, q.session_id)

    response = fix_informix_sql(response)
    response = clean_sql(response)


    return {"response": response}

# ------------------- STREAM -------------------
@app.post("/ask-stream")
@limiter.limit("5/minute")
def ask_stream(q: Question, request: Request):

    def generate():
        try:
            if q.session_id not in sessions:
                sessions[q.session_id] = []

            chat_history = sessions[q.session_id]

            chat_history.append({
                "role": "user",
                "content": q.question
            })

            messages = [system_prompt] + chat_history[-MAX_HISTORY:]

            stream = client.chat.completions.create(
                model=MODEL_NAME,
                messages=messages,
                stream=True
            )

            full_reply = ""

            for chunk in stream:
                content = chunk.choices[0].delta.content or ""
                full_reply += content
                yield content

            chat_history.append({
                "role": "assistant",
                "content": full_reply
            })

        except Exception as e:
            yield f"\n[ERROR]: {str(e)}"

    return StreamingResponse(generate(), media_type="text/plain")

